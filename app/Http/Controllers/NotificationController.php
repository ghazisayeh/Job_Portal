    {
        $userLogin = auth()->user()->id;
        $data = DB::table('notifications')
        ->select('notifications.notificationcontent' , 'notifications.created_at')
        ->where('notifications.id_user' , '=' , $userLogin)->get();
        return view('notifications.notificationList', compact('data'));
    }

    public function clearNotificaton(){
        $userLogin = auth()->user()->id;
        Notification::where('id_user' , '=' , $userLogin)->delete();
        return redirect()->route('notification.index')->with('clearNotification' , 'Notification table successfully cleared');
    }

